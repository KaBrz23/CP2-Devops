-- Criação da tabela TabelaItemPedido
CREATE TABLE TabelaItemPedido (
    Id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY START WITH 1 INCREMENT BY 1 PRIMARY KEY,
    nome NVARCHAR2(2000) NOT NULL,
    descricao NVARCHAR2(2000) NOT NULL,
    quantidade NUMBER(10) NOT NULL,
    preco DECIMAL(18, 2) NOT NULL,
    recomendacao NVARCHAR2(2000) NOT NULL
);

-- Criação da tabela TabelaPagamento
CREATE TABLE TabelaPagamento (
    Id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY START WITH 1 INCREMENT BY 1 PRIMARY KEY,
    num_cartao NVARCHAR2(2000) NOT NULL,
    nome_cartao NVARCHAR2(2000) NOT NULL,
    data_validade NVARCHAR2(2000) NOT NULL,
    cvv NUMBER(10) NOT NULL,
    apelido_cartao NVARCHAR2(2000) NOT NULL
);

-- Criação da tabela TabelaPedido
CREATE TABLE TabelaPedido (
    Id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY START WITH 1 INCREMENT BY 1 PRIMARY KEY,
    ValorTotal DECIMAL(18, 2) NOT NULL,
    FreteEntrega DECIMAL(18, 2) NOT NULL,
    DataPedido NVARCHAR2(2000) NOT NULL,
    DataEntrega NVARCHAR2(2000) NOT NULL
);

-- Criação da tabela TabelaUsuario
CREATE TABLE TabelaUsuario (
    Id NUMBER(10) GENERATED BY DEFAULT AS IDENTITY START WITH 1 INCREMENT BY 1 PRIMARY KEY,
    nome NVARCHAR2(2000) NOT NULL,
    email NVARCHAR2(2000) NOT NULL,
    senha NVARCHAR2(2000) NOT NULL,
    telefone NVARCHAR2(2000) NOT NULL,
    endereco NVARCHAR2(2000) NOT NULL,
    cep NVARCHAR2(2000) NOT NULL,
    cidade NVARCHAR2(2000) NOT NULL,
    estado NVARCHAR2(2000) NOT NULL,
    cpf NVARCHAR2(2000) NOT NULL,
    dataCadastro NVARCHAR2(2000) NOT NULL,
    dataNascimento NVARCHAR2(2000) NOT NULL,
    sexo NVARCHAR2(2000) NOT NULL
);
